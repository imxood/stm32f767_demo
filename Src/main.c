//重定义fputc函数到串口
int fputc(int ch, FILE *f)
{
    while ((USART1->ISR & 0X40) == 0)
        ; //循环发送,直到发送完毕
    USART1->TDR = (uint8_t)ch;
    return ch;
}

// 接收中断回调函数
